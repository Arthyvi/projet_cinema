    //Test si le login exist dans sa bonne base de données : soit users, soit Clients
    private String TestLoginExist(AtomicInteger bufferIndex)
    {
        String result = "";
        int compt = 0;
        
        if(radioButtonEmploye.isSelected())
        {
            for(ArrayList<String> row : UsersList)
            {
                if(row.get(2).equals(jTextFieldIdentifiant.getText().trim()))
                {
                    result = row.get(3);
                    bufferIndex.set(compt);
                   
                    break;
                }
                
                compt++;
            }
            
        }
        else
        {
            
            for(ArrayList<String> row : ClientsList)
            {
                if(row.get(2).equals(jTextFieldIdentifiant.getText()))
                {
                    result = row.get(3);
                    bufferIndex.set(compt);
                    break;
                }
                
                compt++;
            }
            
        }
        
        return result;
    }
    
