    private boolean checkConditions(boolean Printing)
    { 
        boolean good = true;
        
        jLabelError.setText("");
        jLabel3.setText("");
        
        if((radioButtonEmploye.isSelected() == false)&&(radioButtonClient.isSelected() == false))
        {
            if (Printing == true)
                jLabelWarningRadioButton.setText("Selectionner une des deux catégories!!!");
            good = false;
        }
        else
        {
            jLabelWarningRadioButton.setText("");
        }
        
        if(("".equals(jTextFieldIdentifiant.getText()))||("Entrez votre identifiant".equals(jTextFieldIdentifiant.getText())))
        {
            if (Printing == true)
            {
                jLabelError.setText("'''");
            }
            
             good = false;
             
        }
        else
        {
            jLabelError.setText("");
        }
        
        if("".equals(Utilitaires.charToString(jPasswordField1.getPassword())))
        {
            if (Printing == true)
            {
                jLabelError.setText("'''");
            }
             good = false;
        }
        else
        {
            jLabelError.setText("");
        }
        
        
        
       return  good;
    }
    
